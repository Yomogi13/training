package text.section05;

public class variable04 {
	public static void main(String [] args) {
		
//		整数型と浮動小数点の足し算
		int number1 = 5;
		float number2 = 2.5F;
		System.out.println(number1 + number2);
		
//		文字列型と文字列型の連結
		String lastName = "テスト";
		String firstName = "太郎";
		System.out.println(lastName + firstName);
		
	}

}
