//四則演算
console.log(45 + 18);
console.log(55 - 18);
console.log(85 * 18);
console.log(95 / 18);

//文字列の連結
console.log("45" + "18");
console.log("こんにちは" + "さようなら")

//変数の宣言
let userName;

//値の代入
userName = "チョモランマ松沢"

//変数の宣言と値の代入（同時に行うやつ）
let userNumber = 55;

//コンソールへの出力
console.log(userName);
console.log(userNumber);

//値の再代入（中身を変えるやつ）
userName = "エレファント藤原"
userNumber = 88;

//コンソールへの再出力（中身を変えたやつ）
console.log(userName);
console.log(userNumber);

//定数の宣言・値の代入
const shippingFee = 500;

//コンソールへの出力
console.log(shippingFee);

/* 
変数；後から中身を入れ替えられる（再代入できる）
定数；後から中身を入れ替えられない（再代入できない）
基本的には定数（const)を使う」のが一般的
*/

